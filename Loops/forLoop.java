package Loops;

public class forLoop {
    public static void main(String[] args) {
        for (int i = 0; i < 9; i++) {
            System.out.println(i + " This will print the nos till 9 ");
        }

        for(int i = 0; i < 69; i = i + 2){
            System.out.println(i + " is the Even nos between 0 and 69");
        }
    }
}
