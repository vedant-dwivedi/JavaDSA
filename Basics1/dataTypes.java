package Basics1;

public class dataTypes {
    public static void main(String[] args) {
        int num = 69;
        long numl = 6912369L;
        double numd = 6.912369;
        float numf = 6.9f;

        System.out.println(num);
        System.out.println(numl);
        System.out.println(numd);
        System.out.println(numf);
    }
}