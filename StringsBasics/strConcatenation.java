package StringsBasics;

public class strConcatenation {
    public static void main(String[] args) {
        String str1 = "Vedant";
        String str2 = "Dwivedi";
        String str3 = "Web ";
        String str4 = "Developer";
        System.out.println(str1 + " " + str2);
        System.out.println(str1 + " " + str2);
        
        System.out.println(str3.concat(str4));
    }
}